#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	if (s1 != NULL && s2 != NULL)
		if (*s1 == *s2)
			return (*s1 == '\0' ? 1 : ft_strequ((s1 + 1), (s2 + 1)));
	return (0);
}
