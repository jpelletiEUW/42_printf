#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t i;

	if (!s1 || !s2)
		return (0);
	i = -1;
	while (s1[i] == s2[i] && i + 1 < n)
	{
		i++;
		if (s1[i] != s2[i])
			return (0);
	}
	return (1);
}
